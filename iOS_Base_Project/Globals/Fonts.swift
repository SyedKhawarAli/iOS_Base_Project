import Foundation
import UIKit

enum Font {
    static func brownBold(_ size: CGFloat) -> UIFont {
        return UIFont(name: "BrownStd-Bold", size: size)!
    }

    static func brownRegular(_ size: CGFloat) -> UIFont {
        return UIFont(name: "BrownStd-Regular", size: size)!
    }
}
